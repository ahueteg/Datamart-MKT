SELECT substring(VBRK_FKDAT,1,6) PERIODO,
  sum(ZVALVTA_UNI) ZVALVTA_UNI,
sum(ZIMP_BRUTO) ZIMP_BRUTO,
sum(ZMARGEN) ZMARGEN,
sum(ZVAL_VTAIMP) ZVAL_VTAIMP,
sum(ZIMP_IMPTO) ZIMP_IMPTO,
sum(DESC_QS) DESC_QS,
  sum(ZVALNETO_SF_QS) ZVALNETO_SF_QS,
  SUM(ZVALNETO) ZVALNETO,
  SUM(ZVALNET_CFLETE) ZVALNET_CFLETE,
  SUM(VAL_DESC_NR) VAL_DESC_NR,
  SUM(VAL_DESC_QS) VAL_DESC_QS,
  SUM(VAL_DESC_RR) VAL_DESC_RR
FROM mkt.SELLOUT_QZ_HISTW
GROUP BY substring(VBRK_FKDAT,1,6)
ORDER BY 1;





