  VBRK_KUNAG,
  KNA1_STCD1
FROM (
  SELECT
    VBRK_KUNAG,
    KNA1_STCD1,
    COUNT(1) OVER (PARTITION BY VBRK_KUNAG) CONT
  FROM mkt.SELLOUT_QZ_HISTW
  GROUP BY VBRK_KUNAG,KNA1_STCD1
) A
WHERE CONT>1
ORDER BY 1


